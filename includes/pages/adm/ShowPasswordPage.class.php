use Florian\NewStar\classes\HTTP;
use Florian\NewStar\classes\PlayerUtil;

class ShowPasswordPage extends AbstractAdminPage
{
	public static $requireModule = 0;

	function __construct() 
	{
        if (!allowedTo(str_replace(array(dirname(__FILE__), '\\', '/', '.php'), '', __FILE__))) throw new Exception("Permission error!");
		parent::__construct();
	}
		
	function show()
	{
		global $LNG;
        
        $Password	= HTTP::_GP('md5q', '', true);
        
		$this->assign(array(
        	'md5_md5' 		    => $Password,
            'md5_enc' 			=> PlayerUtil::cryptPassword($Password),
		));
		
		$this->display('page.password.default.tpl');
	}
}
